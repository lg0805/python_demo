"""
实例3：文本进度条

========================开始下载========================
100%[**************************************************]
========================下载完成========================
"""
import time

# 打印开始下载
print("{:=^52s}".format("开始下载"))
# 打印进度条
num = 50
for i in range(1, num + 1):
    print("\r{}%[{}{}]".format(i * 2, "*" * i, "." * (num - i)), end="")
    time.sleep(0.06)
# 打印下载完成
print("\n{:=^52s}".format("下载完成"))